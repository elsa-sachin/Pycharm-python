""" Create a python program that prompts the user to enter 2 numbers.
Compare the numbers using comparison operators(>,<,>=,<=,!=,==) and prints out the result of each comparison"""


num1=int(input("Enter the 1st number :"))
num2=int(input("Enter the 2nd number :"))

print(num1,">",num2,"=",num1>num2)
print(num1,"<",num2,"=",num1<num2)
print(num1,">=",num2,"=",num1>=num2)
print(num1,"<=",num2,"=",num1<=num2)
print(num1,"!=",num2,"=",num1!=num2)
print(num1,"==",num2,"=",num1==num2)