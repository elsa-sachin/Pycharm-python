""""OBJECT-ORIENTED PROGRAMMING"""

#3 CONCEPTS

"""CLASS"""

#Base model or blueprint eg:house



"""OBJECT"""

#The thing which you develop using class eg : plan of the house is CLASS
#and the house is the OBJECT



"""REFERENCE"""

#The operations done on the OBJECT are called REFERENCES eg:painting ,interior work done on house


"""EXAMPLE"""

"""CLASS-TV"""
"""OBJECT-Samsung,philips,mi.."""
"""REFERNCES-On/Off ,Vul up/down.."""


"""CLASS-PHONES"""
"""OBJECT-iPHONE,Samsung.."""
"""REFERNCES-On/Off ,Vul up/down,Call,Photo clicking,chat.."""
